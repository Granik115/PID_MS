%% motor_params.m
% Параметры мотора для автотюнера position-PID (только 3 коэффициента: Kp, Ki, Kd)
% Используется в pid_autotuner.m и в Simulink-модели position_pid_model.slx
% Все значения — реалистичные для маленького гимбального PMSM (типа MGN v2/v3 из твоей MKTI)
clear; clc;

%% ==================== ЭЛЕКТРИЧЕСКИЕ ПАРАМЕТРЫ ====================
Rs   = 0.45;          % Сопротивление фазы, Ом (при 20°C)
Ld   = 1.2e-3;        % Индуктивность по d, Гн
Lq   = 1.2e-3;        % Индуктивность по q, Гн (для поверхностного PMSM Ld ≈ Lq)
Ke   = 0.072;         % Константа ЭДС (потокосцепление), Вб или В/(рад/с)
pp   = 4;             % Количество пар полюсов

%% ==================== МЕХАНИЧЕСКИЕ ПАРАМЕТРЫ ====================
J    = 0.2;        % Момент инерции ротора + камеры, кг·м²
B    = 0.01;        % Коэффициент вязкого трения, Н·м·с/рад
Tc   = 0.008;         % Момент сухого (кулонова) трения, Н·м

%% ==================== НОМИНАЛЬНЫЕ ПАРАМЕТРЫ ====================
U_n  = 24;            % Номинальное напряжение, В
I_n  = 2.5;           % Номинальный ток, А
M_n  = 0.35;          % Номинальный момент, Н·м
n_n  = 3000;          % Номинальная скорость, об/мин

%% ==================== ТЕМПЕРАТУРНЫЕ КОЭФФИЦИЕНТЫ ====================
alpha_Rs = 0.0039;    % Температурный коэффициент сопротивления меди, 1/°C

%% ==================== ПАРАМЕТРЫ ДЛЯ РЕЛЕЙНОГО ТЮНИНГА ====================
h_relay = 20;        % было 0.8 → делаем сильно больше
sim_time = 1000;        % Время симуляции для одного цикла тюнинга, сек
Ts       = 0.001;     % Шаг дискретизации модели, сек

%% ==================== НАЧАЛЬНЫЕ PID (до первого тюнинга) ====================
Kp_init = 8.0;        % Начальные коэффициенты (можно оставить 0, если хочешь чистый тюнинг)
Ki_init = 0.5;
Kd_init = 1.2;

%% ==================== ДОПОЛНИТЕЛЬНЫЕ ФЛАГИ ====================
enable_temp_correction = true;   % Включить коррекцию Rs по температуре
T_current = 25;                  % Текущая температура мотора, °C

fprintf('✅ motor_params.m загружен. Параметры мотора готовы.\n');